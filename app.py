# ─────────────────────────────────────────────────────────
#  OLLAMA HELPER
# ─────────────────────────────────────────────────────────
def ai_say(plain_text: str) -> str:
    """
    Ask Ollama to make a message sound friendlier.
    If Ollama is not running, returns the plain text as-is.
    """
    if not USE_OLLAMA:
        return plain_text
    try:
        resp = requests.post(
            OLLAMA_URL,
            json={
                "model": OLLAMA_MODEL,
                "prompt": (
                    "You are a Hard IT support assistant. "
                    "Rephrase the following message to sound friendly and professional. "
                    "Keep it SHORT (2-3 sentences max). Do not add extra information. "
                    "Message: " + plain_text
                ),
                "stream": False,
            },
            timeout=20,
        )
        result = resp.json().get("response", "").strip()
        return result if result else plain_text
    except Exception:
        return plain_text   # Ollama not running — use plain text

# ─────────────────────────────────────────────────────────
#  SCRIPT GENERATOR
# ─────────────────────────────────────────────────────────
def make_sh_script(software_key: str, config: dict) -> str:
    """Generate a .sh installation script for a given software."""
    sw = config["software"][software_key]
    lines = [
        "#!/bin/bash",
        "#" + "=" * 50,
        f"# Install script: {sw['display_name']}",
        f"# Description:    {sw['description']}",
        "# Generated by:   IT Install Agent",
        "#" + "=" * 50,
        "",
        "set -e   # Stop script if any command fails",
        "echo '🚀 Starting installation...'",
        "",
    ]
    # Optional: pre-install steps (e.g. apt update)
    for step in sw.get("pre_install", []):
        lines.append(step)
    if sw.get("pre_install"):
        lines.append("")

    # Main install commands
    for cmd in sw["commands"]:
        lines.append(f"echo '➤ Running: {cmd}'")
        lines.append(cmd)
    lines.append("")

    # Optional: post-install steps (e.g. enable service)
    for step in sw.get("post_install", []):
        lines.append(step)
    if sw.get("post_install"):
        lines.append("")

    lines.append("echo '✅ Done! Installation complete.'")
    return "\n".join(lines)

# ─────────────────────────────────────────────────────────
#  SSH AUTO-INSTALLER
# ─────────────────────────────────────────────────────────
def ssh_install(ip, username, password, software_key, config):
    """
    SSH into the target machine and run install commands.
    Returns (success: bool, log: str)
    """
    sw = config["software"][software_key]
    all_commands = (
        sw.get("pre_install", []) +
        sw["commands"] +
        sw.get("post_install", [])
    )
    log_lines = []
    try:
        client = paramiko.SSHClient()
        client.set_missing_host_key_policy(paramiko.AutoAddPolicy())
        client.connect(ip, username=username, password=password, timeout=15)

        for cmd in all_commands:
            log_lines.append(f"$ {cmd}")
            _, stdout, stderr = client.exec_command(cmd, get_pty=True)
            out = stdout.read().decode(errors="replace").strip()
            err = stderr.read().decode(errors="replace").strip()
            if out:
                log_lines.append(out)
            if err and "warning" not in err.lower():
                log_lines.append(f"[stderr] {err}")
            log_lines.append("")

        client.close()
        return True, "\n".join(log_lines)
    except Exception as e:
        return False, str(e)

# ─────────────────────────────────────────────────────────
#  SESSION HELPERS
# ─────────────────────────────────────────────────────────
def init_session():
    """Reset the conversation to the beginning."""
    session["state"]    = "ask_name"
    session["name"]     = ""
    session["software"] = ""
    session["method"]   = ""
    session["ip"]       = ""
    session["username"] = ""
    session["password"] = ""
    session["script"]   = ""

# ─────────────────────────────────────────────────────────
#  MAIN STATE MACHINE
#  ─────────────────────────────────────────────────────────
#  States (in order):
#    ask_name → ask_software → ask_method
#      → (manual) → done
#      → (auto) → ask_ip → ask_username → ask_password → done
#
#  To ADD A NEW STEP in future, just add a new state here
#  and handle it below. The config.json controls software list.
# ─────────────────────────────────────────────────────────
def process_message(user_msg: str) -> dict:
    config   = load_config()
    state    = session.get("state", "ask_name")
    user_msg = user_msg.strip()

    # ── STATE: ask_name ──────────────────────────────────
    if state == "ask_name":
        name = user_msg.title()
        session["name"]  = name
        session["state"] = "ask_software"

        sw_list = "\n".join(
            f"{i+1}. {v['display_name']} — {v['description']}"
            for i, (_, v) in enumerate(config["software"].items())
        )
        reply = (
            f"Nice to meet you, {name}! 👋\n\n"
            f"Here is the list of some available software:\n\n"
            f"{sw_list}\n\n"
            f"Please type the number or the name of the software you need."
        )
        return {"reply": reply, "type": "text"}

    # ── STATE: ask_software ──────────────────────────────
    if state == "ask_software":
        sw_keys  = list(config["software"].keys())
        sw_names = [v["display_name"].lower() for v in config["software"].values()]
        chosen   = None

        if user_msg.isdigit():
            idx = int(user_msg) - 1
            if 0 <= idx < len(sw_keys):
                chosen = sw_keys[idx]
        else:
            for i, name in enumerate(sw_names):
                if user_msg.lower() in name:
                    chosen = sw_keys[i]
                    break

        if not chosen:
            sw_list = "\n".join(
                f"{i+1}. {v['display_name']}"
                for i, (_, v) in enumerate(config["software"].items())
            )
            return {
                "reply": f"Sorry, I did not recognise that. Please type the number from the list:\n\n{sw_list}",
                "type": "text",
            }

        session["software"] = chosen
        session["state"]    = "ask_method"
        sw_name = config["software"][chosen]["display_name"]

        reply = (
            f"You selected: **{sw_name}** ✅\n\n"
            f"How would you like to proceed?\n\n"
            f"**1.** 📄 Send me the **.sh script** — I will run it myself\n"
            f"**2.** 🤖 **Install automatically** — Connect to my machine via SSH(Only if you are available in office or connected via Daffodil VPN\n\n"
            f"Please type **1** or **2**."
        )
        return {"reply": reply, "type": "text"}

    # ── STATE: ask_method ────────────────────────────────
    if state == "ask_method":
        is_manual = "1" in user_msg or any(w in user_msg.lower() for w in ["script", "sh", "manual", "file"])
        is_auto   = "2" in user_msg or any(w in user_msg.lower() for w in ["auto", "ssh", "install", "connect"])

        if is_manual:
            session["method"] = "manual"
            session["state"]  = "done"
            script_text = make_sh_script(session["software"], config)
            session["script"] = script_text
            sw_name = config["software"][session["software"]]["display_name"]
            fname   = f"install_{session['software']}.sh"
            reply = (
                f"Here is your installation script for **{sw_name}**! 🎉\n\n"
                f"**Steps to use it:**\n"
                f"1. Click the download button below to save the file\n"
                f"2. Copy the file to your Ubuntu machine\n"
                f"3. Open a terminal and run these two commands:\n\n"
                f"   `chmod +x {fname}`\n"
                f"   `sudo ./{fname}`\n\n"
                f"That's it! Let me know if you need anything else."
            )
            return {"reply": reply, "type": "download", "filename": fname}

        elif is_auto:
            session["method"] = "auto"
            session["state"]  = "ask_ip"
            
            reply = (
                "I will install it automatically via SSH. 🔐\n\n"
    
                "⚠️ Before proceeding, To make sure SSH is installed and enabled on your machine.\n\n"
    
                "**Run the below commands:**\n"
                "`sudo systemctl enable ssh && sudo systemctl start ssh`\n\n"
                "→ Check firewall: `sudo ufw allow ssh`\n\n"
    
                "⚠️For SSH access your credentials are only used for this session and are **never saved**.\n\n"
    
                "Please enter the **IP address** of your Ubuntu machine\n"
                "you can find via the IP Address by ifconfig or ip a CMD"
                "(example: `172.18.*.*` or `10.0.*.*`):"
        )
        return {"reply": reply, "type": "text"}
       

    # ── STATE: ask_ip ────────────────────────────────────
    if state == "ask_ip":
        if not re.match(r"^\d{1,3}(\.\d{1,3}){3}$", user_msg):
            return {
                "reply": "That does not look like a valid IP address.\nPlease enter it in this format: `192.168.1.100`",
                "type": "text",
            }
        session["ip"]    = user_msg
        session["state"] = "ask_username"
        return {"reply": f"IP address saved: `{user_msg}` ✅\n\nNow please enter the **SSH username** (e.g. `ubuntu`, `admin`, `root`):", "type": "text"}

    # ── STATE: ask_username ──────────────────────────────
    if state == "ask_username":
        session["username"] = user_msg
        session["state"]    = "ask_password"
        return {
            "reply": f"Username: **{user_msg}** ✅\n\nNow please enter the **SSH password**:",
            "type": "text",
            "password_input": True,   # tells the UI to use a password field
        }

    # ── STATE: ask_password ──────────────────────────────
    if state == "ask_password":
        session["password"] = user_msg
        session["state"]    = "installing"
        sw_name = config["software"][session["software"]]["display_name"]

        # Run the installation
        success, log = ssh_install(
            session["ip"],
            session["username"],
            session["password"],
            session["software"],
            config,
        )
        session["state"] = "done"

        if success:
            reply = (
                f"✅ **{sw_name}** has been installed successfully on `{session['ip']}`!\n\n"
                f"**Installation log:**\n```\n{log[:3000]}\n```\n\n"
                f"All done, {session['name']}! Need anything else? Just say **restart** to start over."
            )
            return {"reply": reply, "type": "result", "success": True}
        else:
            reply = (
                f"❌ **Installation failed.**\n\n"
                f"**Error details:**\n```\n{log}\n```\n\n"
                f"Please check:\n"
                f"- Is the IP address correct? (`{session['ip']}`)\n"
                f"- Is SSH enabled on the machine?\n"
                f"- Is the username/password correct?\n\n"
                f"Type **restart** to try again."
            )
            return {"reply": reply, "type": "result", "success": False}

    # ── STATE: done ──────────────────────────────────────
    if state == "done":
        if "restart" in user_msg.lower() or "yes" in user_msg.lower() or "more" in user_msg.lower():
            init_session()
            return {"reply": "Let's start fresh! 😊 What is your name?", "type": "text"}
        return {
            "reply": "Your request is complete. Type **restart** if you need to install something else.",
            "type": "text",
        }

    # Fallback
    return {"reply": "Something went wrong. Please refresh the page.", "type": "text"}

