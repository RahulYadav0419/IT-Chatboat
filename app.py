@app.route("/")
def index():
    """Serve the chat page."""
    if "state" not in session:
        init_session()
    return HTML_PAGE

@app.route("/chat", methods=["POST"])
def chat():
    """Handle incoming chat messages."""
    if "state" not in session:
        init_session()
    data     = request.get_json()
    user_msg = data.get("message", "").strip()
    if not user_msg:
        return jsonify({"reply": "Please type a message.", "type": "text"})
    result = process_message(user_msg)
    return jsonify(result)

@app.route("/download")
def download():
    """Download the generated .sh script."""
    script   = session.get("script", "")
    filename = request.args.get("name", "install.sh")
    if not script:
        return "No script available.", 404
    buf = io.BytesIO(script.encode("utf-8"))
    buf.seek(0)
    return send_file(buf, as_attachment=True, download_name=filename, mimetype="text/plain")

@app.route("/reset")
def reset():
    """Reset the session."""
    init_session()
    return jsonify({"ok": True})

